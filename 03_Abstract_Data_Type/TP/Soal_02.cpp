#include <iostream>
using namespace std;

int main() {
    int x = 88;  // Deklarasi variabel x dengan nilai 88

    // Menampilkan nilai variabel
    cout << "Nilai dari x: " << x << endl;

    // Menampilkan alamat memori dari variabel x
    cout << "Alamat memori dari x: " << &x << endl;

    return 0;
}

